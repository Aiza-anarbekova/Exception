package com.compan;

public class Cone {
    private int radius;
    private int vysota;

    public Cone(int radius, int vysota) {
        this.radius = radius;
        this.vysota = vysota;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }

    public int getVysota() {
        return vysota;
    }

    public void setVysota(int vysota) {
        this.vysota = vysota;
    }
}
